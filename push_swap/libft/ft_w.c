#include "libft.h"

int	ft_w(char const *str, char c)
{
	int i;
	int numb;

	i = 1;
	if (str[0] == c)
		numb = 0;
	else
		numb = 1;
	while (str[i])
	{
		if (str[i] != c && str[i - 1] == c)
			numb++;
		i++;
	}
	return (numb);
}
